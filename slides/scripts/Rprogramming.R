Newton<-function(x,a) {x - (x^2-a)/(2*x)}

x<-2
for (i in 1:8){
  print(x,digits=22)
  x<-Newton(x,2)
}

## ------------------------------------------------------------------------
mySqrt<-function(a,start=2,maxIter=10,maxDiff=.0001){
  
  n<-1
  x<-start
  diff=10000
  while(n<maxIter & diff>maxDiff){
    y<-x
    x<-Newton(x,a)
    diff=abs(x-y)
    n<-n+1
  }
  return(list(sqrt=x,iterations=n-1,difference=diff))
}

